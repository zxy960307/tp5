CREATE TABLE o2o_bis(
	id int(11) UNSIGNED NOT NULL AUTO_INCREMENT,
	city_id int(11) UNSIGNED NOT NULL DEFAULT 0,
	city_path VARCHAR(50) NOT NULL DEFAULT '',
	name VARCHAR(20) NOT NULL DEFAULT '',
	email VARCHAR(20) NOT NULL DEFAULT '',
	logo VARCHAR(255) NOT NULL DEFAULT '',
	licence_image VARCHAR(255) NOT NULL DEFAULT '',
	description text NOT NULL,
	bank_count int(12) UNSIGNED NOT NULL DEFAULT 0,
	bank_name VARCHAR(20) NOT NULL DEFAULT '',
	bank_user VARCHAR(20) NOT NULL DEFAULT '',
	bank_money decimal(8,2) NOT NULL DEFAULT 0.00,
	faren VARCHAR(20) NOT NULL DEFAULT '',
	faren_tel INT(11) NOT NULL DEFAULT 0,
	faren_email VARCHAR(20),
	listorder int(8) UNSIGNED NOT NULL DEFAULT 0,
	status tinyint(1) NOT NULL DEFAULT 0,
	create_time int(11) UNSIGNED NOT NULL DEFAULT 0,
	update_taime INT(11) UNSIGNED NOT NULL DEFAULT 0,
	PRIMARY KEY(id),
	KEY city_id(city_id),
	KEY name(name)	
)ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=UTF8;